                    "Pengesahan RUU Pemilu ini adalah langkah maju bagi demokrasi kita. Terdapat beberapa penyesuaian terkait ambang batas parlemen dan sistem kampanye digital," ujar Ketua Panja RUU Pemilu.
                </blockquote>

                <h2>Poin-Poin Penting Perubahan</h2>
                <p>Salah satu perubahan paling signifikan dalam aturan baru ini adalah pemotongan masa kampanye dari yang sebelumnya 6 bulan menjadi hanya 75 hari. Hal ini bertujuan untuk menekan biaya politik serta mengurangi potensi polarisasi yang berkepanjangan di masyarakat.</p>

                <p>Selain itu, aturan mengenai penggunaan kecerdasan buatan (AI) dalam materi kampanye juga diatur secara ketat. Peserta pemilu diwajibkan menyertakan label 'Generated by AI' pada setiap konten visual maupun audio yang dibuat menggunakan teknologi tersebut.</p>

                <p>Meski telah disahkan, sejumlah lembaga swadaya masyarakat (LSM) yang berfokus pada pemilu menyatakan akan mengajukan *judicial review* ke Mahkamah Konstitusi terkait pasal pembatasan kampanye independen.</p>

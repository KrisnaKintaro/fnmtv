                <p><strong>JAKARTA, FNM</strong> — Dewan Perwakilan Rakyat (DPR) secara resmi telah mengesahkan Rancangan Undang-Undang (RUU) Pemilihan Umum menjadi Undang-Undang dalam Rapat Paripurna yang digelar pada Selasa siang.</p>

                <p>Pengesahan ini diwarnai dengan berbagai interupsi dari sejumlah fraksi, namun pada akhirnya mayoritas anggota dewan menyetujui draf final yang diajukan oleh Panitia Kerja (Panja).</p>

